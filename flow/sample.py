import numpy as np
import pandas as pd


def add(x: float, y: float) -> float:
    """Return the sum of x and y.

    :param x:                       float value
    :type x:                        float value

    :param y:                       float value
    :type y:                        float value

    :return:                        sum of x and y as a float

    """

    return x + y
